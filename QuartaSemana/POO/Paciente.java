package POO;

public class Paciente {

	String nomePaciente;
	int idadePaciente;
	String sexoPaciente;
	double alturaPaciente;
	double pesoPaciente;
	String patologiaPaciente;
	
	
	
	public String getNomePaciente() {
		return nomePaciente;
	}
	public void setNomePaciente(String nomePaciente) {
		this.nomePaciente = nomePaciente;
	}
	public int getIdadePaciente() {
		return idadePaciente;
	}
	public void setIdadePaciente(int idadePaciente) {
		this.idadePaciente = idadePaciente;
	}
	public String getSexoPaciente() {
		return sexoPaciente;
	}
	public void setSexoPaciente(String sexoPaciente) {
		this.sexoPaciente = sexoPaciente;
	}
	public double getAlturaPaciente() {
		return alturaPaciente;
	}
	public void setAlturaPaciente(double alturaPaciente) {
		this.alturaPaciente = alturaPaciente;
	}
	public double getPesoPaciente() {
		return pesoPaciente;
	}
	public void setPesoPaciente(double pesoPaciente) {
		this.pesoPaciente = pesoPaciente;
	}
	public String getPatologiaPaciente() {
		return patologiaPaciente;
	}
	public void setPatologiaPaciente(String patologiaPaciente) {
		this.patologiaPaciente = patologiaPaciente;
	}
	
	
	
}
